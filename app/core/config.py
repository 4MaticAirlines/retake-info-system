"""
Конфигурация приложения «Retake Info System».

Назначение модуля
-----------------
Централизованное определение всех путей, URL-адресов внешних ресурсов
и параметров подключения к базе данных. Параметры читаются из
файла .env в корне проекта, что позволяет настраивать развёртывание
без правки исходного кода.

Константы каталогов
-------------------
- ``BASE_DIR``: корневая директория проекта (родитель app/).
- ``DATA_DIR``: директория данных (data/).
- ``INPUT_DIR``: входные Excel-файлы кафедр.
- ``STATEMENT_DIR``: PDF-выписки студентов.
- ``OUTPUT_DIR``: выходные артефакты модели (графики, метрики).

Параметры из .env
-----------------
- ``DATABASE_URL``: URL подключения к СУБД через SQLAlchemy.
- ``UNIVERSITY_RETAKES_URL``: страница вуза со вторичными пересдачами.
- ``UNIVERSITY_SESSION_URL``: страница основной сессии.
- ``SERVER_HOST``: адрес для биндинга сервера (по умолчанию 0.0.0.0).
- ``SERVER_PORT``: порт сервера (по умолчанию 8000).
- ``PUBLIC_DOMAIN``: публичный домен сервиса (если развёрнут наружу).
- ``ALLOWED_HOSTS``: список доменов, которые разрешено принимать.
"""
import os
from pathlib import Path

from dotenv import load_dotenv


# Корневая директория проекта. Используется для построения абсолютных
# путей к ресурсам, чтобы приложение работало независимо от cwd.
BASE_DIR = Path(__file__).resolve().parent.parent.parent

# Загружаем переменные окружения из .env в корне проекта.
# Если файла нет — используются значения по умолчанию.
load_dotenv(BASE_DIR / ".env")

# Директории данных. Создаются автоматически при первом запуске.
DATA_DIR = BASE_DIR / "data"
INPUT_DIR = DATA_DIR / "input" / "excel_files"
STATEMENT_DIR = DATA_DIR / "input" / "statements"
OUTPUT_DIR = DATA_DIR / "output"

INPUT_DIR.mkdir(parents=True, exist_ok=True)
STATEMENT_DIR.mkdir(parents=True, exist_ok=True)
OUTPUT_DIR.mkdir(parents=True, exist_ok=True)


# ---------- Параметры базы данных ----------

# URL подключения к базе данных в формате SQLAlchemy.
# По умолчанию используется SQLite-файл в корне проекта.
DATABASE_URL = os.getenv("DATABASE_URL", "sqlite:///./retake.db")


# ---------- URL внешних ресурсов ----------

# Страница ликвидации задолженностей вуза (вторичные/комиссия).
# Если пуста — автозагрузка по этому источнику не выполняется.
UNIVERSITY_RETAKES_URL = os.getenv("UNIVERSITY_RETAKES_URL", "").strip()

# Страница актуальной экзаменационной сессии (основная попытка).
UNIVERSITY_SESSION_URL = os.getenv(
    "UNIVERSITY_SESSION_URL",
    "https://misis.ru/students/session/",
).strip()


# ---------- Параметры сетевого развёртывания ----------

# Адрес, на котором сервер слушает входящие соединения.
# 0.0.0.0 — все интерфейсы (нужно для доступа из локальной сети
# и интернета при настройке домена).
# 127.0.0.1 — только локальные подключения (безопаснее для разработки).
SERVER_HOST = os.getenv("SERVER_HOST", "0.0.0.0")

# Порт, на котором работает сервер. Стандартный для веб-сервисов — 8000.
# При публикации в интернет рекомендуется проксирование через nginx
# на порт 80 (HTTP) или 443 (HTTPS).
SERVER_PORT = int(os.getenv("SERVER_PORT", "8000"))

# Публичный домен сервиса. Заполняется после привязки домена.
# Используется для формирования абсолютных URL в письмах, ссылках,
# документах и других внешних коммуникациях.
# Пример: "retake.example.com" (без протокола и слеша).
PUBLIC_DOMAIN = os.getenv("PUBLIC_DOMAIN", "").strip()

# Список доменов и IP, с которых разрешены входящие запросы.
# Через запятую без пробелов. Wildcard «*» разрешает любые хосты
# (только для разработки!).
ALLOWED_HOSTS = [
    h.strip()
    for h in os.getenv("ALLOWED_HOSTS", "*").split(",")
    if h.strip()
]
